package center.kit.app.homework.lesson4.utils.convertors;

public class PrimitiveConvertor {

    public char floatToChar(float myFloat) {
        char result = (char) myFloat;
        return result;
    }

    public char intToChar(int myInt) {
        char result = (char) myInt;
        return result;
    }

    public int charToInt(char myChar) {
        int result = myChar;
        return result;
    }
}
